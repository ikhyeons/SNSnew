package memo;

public class Memo {
	private String memoNum;
	private String userEmail;
	private String memoTitle;
	private String memoMakeDate;
	private String memoContent;
	private String memoDelete;
	
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getMemoTitle() {
		return memoTitle;
	}
	public void setMemoTitle(String memoTitle) {
		this.memoTitle = memoTitle;
	}
	public String getMemoMakeDate() {
		return memoMakeDate;
	}
	public void setMemoMakeDate(String memoMakeDate) {
		this.memoMakeDate = memoMakeDate;
	}
	public String getMemoContent() {
		return memoContent;
	}
	public void setMemoContent(String memoContent) {
		this.memoContent = memoContent;
	}
	public String getMemoDelete() {
		return memoDelete;
	}
	public void setMemoDelete(String memoDelete) {
		this.memoDelete = memoDelete;
	}
	public String getMemoNum() {
		return memoNum;
	}
	public void setMemoNum(String memoNum) {
		this.memoNum = memoNum;
	}
	

	
	
}
